def count_trees(map, down, right):
    global TREE
    trees_encountered = 0
    cur_x = cur_y = 0
    while cur_y < len(map):
        if map[cur_y][cur_x] == TREE:
            trees_encountered += 1
        cur_x += right
        if cur_x >= len(map[0]):
            cur_x -= len(map[0])
        cur_y += down
    return trees_encountered


